
package main

import "fmt"

func main() {

  
  fmt.Println("Go - Operators")
  fmt.Println("Ass O")

  num1 := 60 
  num2 := 13 
  fmt.Printf("%v \n ",num1)
  num1 += num2
  fmt.Printf("%v \n ",num1)
  num1 -= num2
  fmt.Printf("%v \n ",num1)
  num1 *= num2
  fmt.Printf("%v \n ",num1)
  num1 /= num2
  fmt.Printf("%v \n ",num1)
  num1 %= num2
  fmt.Printf("%v \n ",num1)
    
} 
